package model;

/**
 *
 * @author Aluno
 */
public class Cargo {
    private int id;
    private int areaId;
    private String nome;

    public Cargo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAreaId() {
        return areaId;
    }

    public void setAreaId(int areaId) {
        this.areaId = areaId;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public String toString() {
        return "Cargo{" +
                "id=" + id +
                ", areaId=" + areaId +
                ", nome='" + nome + '\'' +
                '}';
    }
}
